export const getIconSize = (size) => {
  switch (size) {
    case 'small': {
      return '14px'
    }

    default: {
      return '16px'
    }
  }
}

export const getFontSize = (size) => {
  switch (size) {
    case 'small': {
      return '12px'
    }

    default: {
      return '14px'
    }
  }
}
